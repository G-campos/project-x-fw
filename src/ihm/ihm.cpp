void ihm_loop(){

  switch (screenShowing) {
  case MAIN:
	if (!hasShow) {
		main_screen();
		hasShow = true;
	}
	if (!hasConnection) {
		screenShowing = QRCODE;
		hasShow = false;
  	}
	break;
  
  case QRCODE:
    if (!hasShow) {
		qrcode_screen();
		hasShow = true;
	}
  	if (hasConnection) {
		screenShowing = MAIN;
		hasShow= false;
  	}

	break;
  
  default:
	break;
  }

//   delay(3000);
}
